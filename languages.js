function translate() {
	lang = (document.URL.match(/lang=(..)/) || [0, 'en'])[1];

	$('.trans').each(function() {
		$this = $(this)
		//.log("Hola", $this.data('text'))
		if (this.tagName == 'IMG') $this.attr('title' ,texts[$this.data('text')][lang]);
		else $this.html(texts[$this.data('text')][lang]);
	})
	$('.imgTrans').each(function() {
		$this = $(this)
		if (this.tagName == 'IMG') $this.attr('src',  texts[$this.attr('src')][lang])
		else $this.attr('href',  texts[$this.attr('href')][lang]).show()
	})
	if (lang == 'en') $('#donatebutton').attr('src', $('#donatebutton').attr('src').replace('es_ES/ES', 'en_US/'))

	$('#'+lang).css('font-weight', 'bold')
}

